  "$VIGILIA_RUNTIME_DIR" \
  "$VIGILIA_RUN_DIR" \
  "$VIGILIA_LOG_DIR" \
  "$VIGILIA_AUDIO_DIR" \
  "$VIGILIA_ASTERISK_ETC_DIR" \
  "$VIGILIA_ASTERISK_VAR_DIR"
