export VIGILIA_RUNTIME_DIR="${VIGILIA_RUNTIME_DIR:-$PROJECT_DIR/.runtime}"
export VIGILIA_RUN_DIR="${VIGILIA_RUN_DIR:-$VIGILIA_RUNTIME_DIR/run}"
